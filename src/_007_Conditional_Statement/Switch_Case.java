package _007_Conditional_Statement;
// Switch Case
public class Switch_Case {
    public static void main(String[] args) {
        int number = 3;
        switch (number) {
            case 1:
                System.out.println("Burger");
                break;
            case 2:
                System.out.println("Momos");
                break;
            case 3:
                System.out.println("Daaru");
                break;
            default:
                System.out.println("wake up");
        }
    }
}
/*
Output:
Daaru
 */
