package _009_Loops;
// print 1 to 50 numbers using of While Loops in Horizontal
public class _04printNumber_whileLoop {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 50) {
            System.out.print(i+" ");
            i++;
        }
        System.out.println();
    }
}
